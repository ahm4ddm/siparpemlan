    private void bt_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_loginActionPerformed
        // TODO add your handling code here:
        String username = in_username.getText();
        String password = in_password.getText();
        boolean isMasuk = false;
        for (int i = 0; i < Arrusers.size(); i++) {
            if(sistemLogin.Arrusers.get(i).getUname().equals(in_username.getText()) && sistemLogin.Arrusers.get(i).getPass().equals(in_password.getText())){
                isMasuk = true;
            }
        }
        if(isMasuk == true){
            this.setVisible(false);
            formParkir parkirPage = new formParkir();
            parkirPage.setResizable(false);
            parkirPage.setVisible(true);
        }else if (username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "username atau password tidak boleh kosong!", "Peringatan",2);
        }else{
            JOptionPane.showMessageDialog(this, "username atau password salah!", "Peringatan", 2);
        }
    }//GEN-LAST:event_bt_loginActionPerformed

